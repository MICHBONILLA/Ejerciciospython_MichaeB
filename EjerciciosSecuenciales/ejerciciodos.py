# Se declara la variable base, Se declara la variable altura, e declara la variable perimetro multiplicando base * 2 y altura*2 y se suman,se declara la variable area multiplicando la base por la altura y se muestra el resultado mediante print concatenando las variables perimetro y el area

# Se declara la variable base
base= int(input (" Ingrese la base"))
# Se declara la variable altura
altura= int(input (" Ingrese la altura"))
# Se declara la variable perimetro multiplicando base * 2 y altura*2 y se suman
perimetro= (base*2) + (altura*2)
# se declara la variable area multiplicando la base por la altura
area= base*altura
# se muestra el resultado mediante print concatenando las variables perimetro y el area
print (" El perimetro del rectangulo es: ", perimetro, " y su area es:", area)